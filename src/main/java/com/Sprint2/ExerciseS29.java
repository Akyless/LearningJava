package com.Sprint2;

public class ExerciseS29 {

    public static void main(String[] args) {
        for (int a = 1; a <= 9; a++) {
            for (int b = 1; b <= 9; b++) {
                System.out.println(a + "*" + b + "=" + (a*b));
            }
            System.out.println();
        }
    }
}
